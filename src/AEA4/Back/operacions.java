package AEA4.Back;

public class operacions {

    public double suma(double num1, double num2) {
        double resultat = 0;
        resultat = num1 + num2;
        return resultat;
    }

    public double restar(double num1, double num2) {
        double resultat = 0;
        resultat = num1 - num2;
        return resultat;
    }

    public double multiplicar(double num1, double num2) {
        double resultat = 0;
        resultat = num1 * num2;
        return resultat;
    }

    public double dividir(double num1, double num2) {
        double resultat = 0;
        resultat = num1 / num2;
        return resultat;
    }
}
